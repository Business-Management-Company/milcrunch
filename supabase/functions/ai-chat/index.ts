import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const SYSTEM_PROMPT = `You are an AI assistant for a comprehensive Events & Awards platform designed for the Military & Veteran community. You help users:

1. **Create Events**: Guide users through creating live, virtual, or hybrid events with ticketing, registration forms, and QR check-in.

2. **Design Awards Programs**: Help users set up awards programs with categories, nominations, judging rubrics, public voting, and winner announcements.

3. **Manage Sponsorships**: Assist with creating sponsor packages, sending proposals, tracking deals, and managing sponsor relationships.

4. **Platform Navigation**: Help users navigate the platform, understand features, and troubleshoot issues.

Key Platform Features:
- AI-powered event and awards creation wizards
- Multiple ticket types (free, paid, donation, VIP, member, sponsor)
- Blind judging with customizable scoring rubrics
- Public voting with IP-based fraud prevention
- Sponsor proposal builder with e-signatures and analytics
- Real-time reporting and analytics
- White-label branding options

Be helpful, concise, and always focused on helping users succeed with their events and awards programs. If users ask about specific features, provide clear guidance on how to use them.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add credits to continue." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "AI service unavailable" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Chat error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
